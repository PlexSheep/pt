//// MACROS ////////////////////////////////////////////////////////////////////////////////////////
/// Quickly get a one line visual divider
#[macro_export]
macro_rules! divider {
    () => {{
        format!("{:=^80}", "=")
    }};
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// Quickly print a one line visual divider
#[macro_export]
macro_rules! println_divider {
    () => {{
        println!("{}", divider!())
    }};
}

